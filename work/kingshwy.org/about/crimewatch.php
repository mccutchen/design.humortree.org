<p>Kings Highway has an active neighborhood association.  The Crime Watch and Code Committee is part of the association and provides regular updates at each meeting.
The neighborhood is currently experiencing a significant downward trend in crime (down 14% overall 2005 vs 2004), due to the vigilance of all of our neighbors.  </p>

<p>The way the neighborhood approaches crime reduction is through a multi-faceted strategy that includes a significant amount of fun and community.  The underlying
principle for crime reduction in our neighborhood is that if we know our neighbors, we'll notice when someone is around who should not be.  We encourage everyone to make
an effort to get to know their immediate neighbors and beyond.  To facilitate people getting to know the neighborhood at large, neighbors host a monthly social hour,
&#8220;First Friday,&#8221; and we typically have 50-70 people attend.  Our community police partners also attend when they are able.</p>

<p>In addition to knowing our neighbors, the Crime Watch and Code Committee stresses another strategy, called the <i>Quick Response Network</i>.This means
that if something is not right, we encourage neighbors to call the police or code agents immediately.  If it is an issue that we need broad support for, we
utilize our email list to disseminate the information quickly.  The <i>Quick Response Network</i> has helped stop illegal building, identify a hit-run culprit, and more.</p>

<p>The final strategy that we employ as a neighborhood is that the Dallas Police and Code Enforcement agents are our friends and partners in making the Kings Highway
neighborhood a better place.  We have been very fortunate to have some of the best people from the city working with us.  </p>

<p>The current co-chairs of the Crime Watch and Code Committee are Val Haskell and Ken Weil.  Kings Highway has block captains who are active participants in the
community.  If you are interested in being a block captain, or if you have a crime or code issue to discuss, please <a href="mailto:crimeandcode@kingshwy.org">contact us</a>.</p>

