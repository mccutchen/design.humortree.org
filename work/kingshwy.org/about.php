        <h4>Becoming a Conservation District</h4>

        <p class="intro"><span>&#8220;We just wanted to stabilize the
        neighborhood,&#8221;</span> said Don Lawrence, a resident of
        the Kings Hwy Neighborhood, back in 1988 when questioned by a
        reporter with the Dallas Morning News.</p>

        <p>He had bought a 1918 prairie-style house around 1983, spent a lot
        of time, effort, and money in renovations, when a large 3-story
        apartment complex was built across the street from him.  It did not
        fit in and was the first step toward compromising the
        neighborhood&#8217;s integrity.  Many other neighbors who had also
        extensively renovated their turn-of-the-century houses, wanted to
        protect the look of the area from new development that did not fit
        in.</p>

        <p>In 1986, a new classification of neighborhoods was adopted,
        known as the <i>conservation district</i>.  The neighbors of Kings Highway petitioned the
        city to have their area classified under this new designation,
        which would restrict the type of development allowed.</p>

        <p>When the Dallas City Council approved their designation,
        Kings Highway became the first conservation district in the city
        of Dallas, and the second in the state of Texas.</p>

        <p>Since then, many other neighborhoods have seen the value in
        this designation, and have followed suit. Some neighborhoods
        are currently waiting their turn for city approval, while
        others are still in the process of gathering signatures
        required for submittal.  It is a long process.</p>

