<div class="left column">
    <p>The name Tudor suggests that these houses imitate English
    architecture from the early 16th century. However, most Tudor style
    homes were inspired by building techniques from an earlier time. They
    may have overlapping gables, parapets, and beautifully patterned brick
    or stonework. These historic details combine with Victorian or
    Craftsman flourishes.</p>

    <p>Tudor style houses often feature striking decorative timbers. These
    timbers hint at, but do not duplicate, Medieval building
    techniques. In Medieval houses, the timber framing was integral with
    the structure. Modern Tudor houses, however, merely suggest the
    structural framework with false half-timbering. This decorative
    woodwork comes in many different designs, with stucco or patterned
    brick between the timbers.</p>

    <p>Tudor styling takes on a variety of forms ranging from elaborate
    mansions to modest homes with mock masonry veneers. The style became
    popular in the 1920s and 1930s, and modified versions became
    fashionable in the 1970s and 1980s.</p>

    <h4>Tudor style homes have many of these features:</h4>
    <ul>
        <li>Decorative half-timbering</li>
        <li>Steeply pitched roof </li>
        <li>Prominent cross gables</li>
        <li>Tall, narrow windows</li>
        <li>Small window panes</li>
        <li>Massive chimneys, often topped with decorative chimney pots</li>
    </ul>
