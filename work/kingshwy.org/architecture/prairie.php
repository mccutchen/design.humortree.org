    <p>The Prairie style is one of the few indigenous American styles.  It
    was developed by an unusually creative group of Chicago architects
    that have come to be know as the Prairie School.  Frank Lloyd Wright's
    early work, inspired by the linearity of Japanese prints, is in this
    style and he is the acknowledged master of the Prairie house.</p>

    <h4>Prairie style homes have many of these features:</h4>
    <ul>
        <li>Low-pitched roofs, usually hipped or gabled with widely overhanging eaves</li>
        <li>Two stories, with on-story wings or porches</li>
        <li>Massive square or rectangular piers of masonry used to support porch roofs</li>
        <li>Rows of casement windows</li>
        <li>Window boxes or flattened pedestal urns for flowers</li>
        <li>Broad, flat chimneys</li>
        <li>Contrasting wall materials or trim, emphasizing second story</li>
        <li>Decorative friezes or door surrounds with floral ornamentation</li>
    </ul>
