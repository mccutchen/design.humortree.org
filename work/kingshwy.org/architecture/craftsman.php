    <p>The name Craftsman comes from designs presented in the artistic and
    popular <i>Craftsman</i> magazine, published by Gustav Stickley from
    1901-1916.  Gradually, however the word took on its own momentum,
    going beyond any specific connection to Stickley or his work, and it
    came to be freely used by others as being characteristic of the period
    and associated with classic bungalows wherever they may be throughout
    the country.</p>

    <h4>Craftsman style bungalows usually have these features:</h4>
    <ul>
        <li>Low-pitched roof</li>
        <li>Wide overhanging eaves</li>
        <li>Street-facing gables with composition shingle roofs</li>
        <li>Decorative braces</li>
        <li>Porch with square columns </li>
        <li>One or one-and-a-half stories</li>
        <li>Built-in cabinets, shelves, and seating</li>
        <li>Always a fireplace</li>
        <li>Casement windows</li>
        <li>Arched opening flanked with bookcases separates living room/dining room </li>
    </ul>
    <h5>Many craftsman sytle bungalows also have:</h5>
    <ul>
        <li>Stone chimneys</li>
        <li>Gabled dormers</li>
        <li>Sloping foundation</li>
    </ul>
